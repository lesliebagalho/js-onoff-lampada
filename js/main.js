lampOff.style.display = "block";

function onclickLampOn(){
    lampOn = document.getElementById("lampOn");
    lampOn.style.display = "block";

    lampOff = document.getElementById("lampOff");
    lampOff.style.display = "none";
}

function onclickLampOff(){
    lampOff = document.getElementById("lampOff");
    lampOff.style.display = "block";

    lampOn = document.getElementById("lampOn");
    lampOn.style.display = "none";
}

